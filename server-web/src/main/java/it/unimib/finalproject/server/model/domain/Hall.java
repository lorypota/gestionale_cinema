package it.unimib.finalproject.server.model.domain;

public class Hall{
    private int id;
    private int rows;
    private int columns;

    public int getId() {
        return id;
    }

    public int getRows() {
        return rows;
    }

    public int getColumns() {
        return columns;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setRows(int rows) {
        this.rows = rows;
    }

    public void setColumns(int columns) {
        this.columns = columns;
    }

    
}
